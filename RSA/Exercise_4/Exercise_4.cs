public class DZ2_Exercise_4
{
    private static int GCD(int a, int b)
    {
        while (b != 0)
        {
            int temp = b;
            b = a % b;
            a = temp;
        }
        return a;
    }
    private static void PrintReducedResidues(int m)
    {
        for (int i = 1; i < m; i++)
        {
            if (GCD(i, m) == 1)
            {
                Console.WriteLine(i);
            }
        }
    }

    public static void Exercise4()
    {
        Console.WriteLine("–í–≤–µ–¥–∏—Ç–µ –º–æ–¥—É–ª—å:");
        int modulus = Convert.ToInt32(Console.ReadLine());
        
        Console.WriteLine("–ü—Ä–∏–≤–µ–¥–µ–Ω–Ω–∞—è —Å–∏—Å—Ç–µ–º–∞ –≤—ã—á–µ—Ç–æ–≤ –ø–æ –º–æ–¥—É–ª—é " + modulus + ":");
        PrintReducedResidues(modulus);
    }
}